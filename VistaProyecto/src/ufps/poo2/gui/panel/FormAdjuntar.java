    private void cmdEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdEnviarActionPerformed
        if (txtPuerto.getText().length() < 4 || (Integer.valueOf(txtPuerto.getText()) < 1023 || Integer.valueOf(txtPuerto.getText()) > 65535)) {
            JOptionPane.showMessageDialog(null, "Ingrese un puerto que este entre 1023 y 65535");
            return;
        }

        if (lblUrl.getText().equalsIgnoreCase(" ")) {
            JOptionPane.showMessageDialog(null, "Seleccione un archivo");
            return;
        }
        try {
            controlador.enviarArchivo(lblUrl.getText(), jugador, Integer.valueOf(txtPuerto.getText()));
            JOptionPane.showMessageDialog(null, "El archivo se esta enviando");
        } catch (Exception e) {
            if (e.getMessage().equalsIgnoreCase("el jugador seleccionado ya no se encuentra conectado")) {
                JOptionPane.showMessageDialog(null, e.getMessage());
                block();
                lblUrl.setText(" ");
                jugador=" ";
                deseleccionar();
                actualizarNombres();
                return;
            }
        }
        block();
        lblUrl.setText(" ");
        jugador=" ";
        deseleccionar();

    }//GEN-LAST:event_cmdEnviarActionPerformed

